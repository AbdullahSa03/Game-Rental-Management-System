    private void getNewData() {

        try {
            rs = dbCon.executeStatement("SELECT DISTINCT store_branch_branch_ID, name FROM game_copy, store_branch WHERE store_branch_branch_ID = branch_ID ORDER BY store_branch_branch_ID ASC");
            ResultSet rs1 = dbCon.executeStatement("SELECT DISTINCT game_game_ID, title FROM game_copy, game WHERE game_game_ID = game_ID ORDER BY game_game_ID ASC");
            try {
                cmbBranchID.removeAllItems();
                cmbGameID.removeAllItems();
                // populate combo boxes              
                while (rs.next()) {     
                    cmbBranchID.addItem(rs.getInt("store_branch_branch_ID") + ": " + rs.getString("name"));
                }
                while (rs1.next()) {
                    cmbGameID.addItem(rs1.getInt("game_game_ID") + ": " + rs1.getString("title"));
                }
                rs.close();
                rs1.close();
            } catch (SQLException ex) {
                Logger.getLogger(ManageGameCopies.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        } catch (SQLException e) {
            javax.swing.JLabel label = new javax.swing.JLabel("SQL Error - Display selected game copy.");
            label.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 18));
            JOptionPane.showMessageDialog(null, label, "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }
