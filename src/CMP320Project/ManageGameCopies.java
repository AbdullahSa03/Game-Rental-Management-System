    // checks if the input is an integer
    public boolean isInteger(String s) { 
        try {
            Integer.parseInt(s);
            return true;
        } catch (NumberFormatException ex) {
            return false;
        }
    }
  
    // validate all user entry before sending new game details to DB
    boolean isValidData(String s) {

        boolean result = true;
        if (s.trim().isEmpty() || !isInteger(s.trim()) || (Integer.parseInt(s.trim()) / 1000 > 9  || Integer.parseInt(s.trim()) < 0)) {
            javax.swing.JLabel label;
            if (s.trim().isEmpty()) {
                label = new javax.swing.JLabel("Cannot be empty.");
            } else if (!isInteger(s.trim())) {
                label = new javax.swing.JLabel("Must be a number.");
            } 
            else {
                label = new javax.swing.JLabel("Must less than 1000 and bigger than or equal to 0.");
            }
            label.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 18));
            JOptionPane.showMessageDialog(null, label, "ERROR", JOptionPane.ERROR_MESSAGE);
            result = false;
        }
        return result;
    }
                
    
    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:

        try {
            if (cmbBranchID.getSelectedItem() != null && cmbGameID.getSelectedItem() != null) {
                String branch_ID = cmbBranchID.getSelectedItem().toString().split(": ")[0];
                String game_ID = cmbGameID.getSelectedItem().toString().split(": ")[0];
                String stmtSQL = "SELECT num_of_copies FROM game_copy WHERE game_game_ID = " + game_ID + " and store_branch_branch_ID = " + branch_ID;
                rs = dbCon.executeStatement(stmtSQL);
                if(rs.first()){
                    String confirm = JOptionPane.showInputDialog("There are currently " + rs.getInt("num_of_copies") + " copies. \nEnter the new amount of copies to use:");
                    if(confirm != null) {
                        if (isValidData(confirm.trim())) {  
                            // execute the update statement
                            String prepSQL = "UPDATE game_copy SET num_of_copies = " + confirm.trim() +
                                    " WHERE game_game_ID = " + game_ID + " and store_branch_branch_ID = " + branch_ID;

                            int result = dbCon.executePrepared(prepSQL);
                            if (result > 0) {                        
                                    javax.swing.JLabel label = new javax.swing.JLabel("Number of copies for branch " + branch_ID + " and game " + game_ID + " updated successfully.");
                                    label.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 18));
                                    JOptionPane.showMessageDialog(null, label, "SUCCESS", JOptionPane.INFORMATION_MESSAGE);  
                                    getNewData();
                            } else {
                                // check validation errors 
                            }
                        }
                    }
                }
                
            } else {
                javax.swing.JLabel label = new javax.swing.JLabel("Please select the branch and game ID's");
                label.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 18));
                JOptionPane.showMessageDialog(null, label, "ERROR", JOptionPane.ERROR_MESSAGE);

            }

        } catch (SQLException e) {

            JOptionPane.showMessageDialog(null, "Error updating game." + e.getMessage());

        }
    }//GEN-LAST:event_btnUpdateActionPerformed


