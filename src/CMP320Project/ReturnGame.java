    private void populateFields() {
        
        try {
            if (!rs.isBeforeFirst() && !rs.isAfterLast()) { // Check if there is a next row
                txtGameTitle.setText(rs.getString("title"));
                txtBranchName.setText(rs.getString("name"));
                txtRentDate.setText(rs.getString("rent_date").substring(0, 10));
                txtReturnDate.setText(rs.getString("return_date").substring(0, 10));
                EnableDisableButtons(); // Enable/disable navigation buttons based on the current position
            } else {
                // No rows in the result set, clear all fields
                txtGameTitle.setText("");
                txtBranchName.setText("");
                txtRentDate.setText("");
                txtReturnDate.setText("");
                // Disable navigation buttons since there are no records to navigate
                btnPrevious.setEnabled(false);
                btnNext.setEnabled(false);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ReturnGame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void MoveNext() { // selecting the next customer and autofilling the textboxes with the customer values
        try {
            // TODO add your handling code here:
            if (!rs.isLast()) {
                rs.next();
                populateFields();
            }
        } catch (SQLException ex) {
            Logger.getLogger(ReturnGame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
