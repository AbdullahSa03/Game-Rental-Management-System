    private void btnReturnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReturnActionPerformed
        // TODO add your handling code here:
        try {
            if (!rs.isBeforeFirst()) {
                // search for active rents with that customer
                String prepSQL1 = "UPDATE game_copy SET num_of_copies = (num_of_copies + 1) "
                            + "WHERE game_game_ID = (SELECT game_ID from game WHERE title LIKE '" + txtGameTitle.getText() + "') "
                            + "AND store_branch_branch_ID = (SELECT branch_ID from store_branch "
                            + "WHERE name LIKE '" + txtBranchName.getText() + "')";
                String prepSQL2 = "DELETE FROM rent_copy "
                        + "WHERE game_ID = (SELECT game_ID from game WHERE title LIKE '" + txtGameTitle.getText() + "') "
                                + "AND branch_ID = (SELECT branch_ID from store_branch "
                                + "WHERE name LIKE '" + txtBranchName.getText() + "')";
                
                ResultSet newRS = dbCon.executeStatement("SELECT rental_price FROM game WHERE title LIKE '" + txtGameTitle.getText() + "'");
                // ask the user if they want to continue
                newRS.first();
                double value = newRS.getDouble("rental_price");

                LocalDate currentDate = LocalDate.now();
                // Convert the string date to LocalDate
                DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
                LocalDate providedDate = LocalDate.parse(txtReturnDate.getText(), formatter);

                // Compare the dates
                if (currentDate.isAfter(providedDate)) {
                    javax.swing.JLabel label = new javax.swing.JLabel("You're returning the game late, a late fee of 50 AED will apply.");
                    value += 50;
                    label.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 18));
                    JOptionPane.showMessageDialog(null, label, "NOTICE", JOptionPane.INFORMATION_MESSAGE);
                }
                String confirm = JOptionPane.showInputDialog("The amount to be paid is " + value + " AED. Type \"yes\" to confirm.");
                confirm += " ";
                if(confirm.equalsIgnoreCase("yes ")) {
                    int result1 = dbCon.executePrepared(prepSQL1);
                    int result2 = dbCon.executePrepared(prepSQL2);
                    if (result1 > 0 && result2 > 0) {
                        javax.swing.JLabel label = new javax.swing.JLabel("Game returned successfully.\nThank you for shopping with us.");
                        label.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 18));
                        JOptionPane.showMessageDialog(null, label, "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                    }
                }
                
                else{
                    JOptionPane.showMessageDialog(null, "Error returning game.");
                }
            }
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error returning game with SQL.");
        }
        getNewData();
    }//GEN-LAST:event_btnReturnActionPerformed

     
