    private void cmbGameIDItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbGameIDItemStateChanged
       
        lblBestSellerGame.setVisible(false);
        if(cmbGameID.getSelectedItem() != null) {
            try{
                ResultSet rs3 = dbCon.executeStatement("SELECT game_ID FROM rent_copy GROUP BY game_ID ORDER BY COUNT(*) DESC FETCH FIRST 1 ROW ONLY");
                
                if (rs3.next() && cmbGameID.getSelectedItem().toString().split(": ")[0].equalsIgnoreCase(rs3.getString("game_ID"))) {
                    lblBestSellerGame.setVisible(true);
                }
                rs3.close();
                ResultSet rs1 = dbCon.executeStatement("SELECT store_branch_branch_ID, name FROM game_copy, store_branch WHERE store_branch_branch_ID = branch_ID AND game_game_ID = " + cmbGameID.getSelectedItem().toString().split(": ")[0] + " AND num_of_copies > 0 ORDER BY store_branch_branch_ID ASC");
                cmbBranchID.removeAllItems();
                while (rs1.next()) {     
                    cmbBranchID.addItem(rs1.getInt("store_branch_branch_ID") + ": " + rs1.getString("name"));
                }
                rs1.close();
            }
            catch (SQLException e) {
                javax.swing.JLabel label = new javax.swing.JLabel("SQL Error - Display selected available branches.");
                label.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 18));
                JOptionPane.showMessageDialog(null, label, "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_cmbGameIDItemStateChanged

    private void cmbBranchIDItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbBranchIDItemStateChanged
        
        lblTopBranch.setVisible(false);
        try {
            ResultSet rs2 = dbCon.executeStatement("SELECT branch_ID FROM rent_copy GROUP BY branch_ID ORDER BY COUNT(*) DESC FETCH FIRST 1 ROW ONLY");
            if (rs2.next() && cmbBranchID.getSelectedItem() != null && cmbBranchID.getSelectedItem().toString().split(": ")[0].equalsIgnoreCase(rs2.getString("branch_ID"))) {
                lblTopBranch.setVisible(true);
            }
            rs2.close();
        } catch (SQLException ex) {
            Logger.getLogger(RentGame.class.getName()).log(Level.SEVERE, null, ex);
        }       
    }//GEN-LAST:event_cmbBranchIDItemStateChanged


