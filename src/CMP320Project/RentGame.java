    private void getNewData() {
        
        clearLabels();
        cmbBranchID.removeAllItems();
        cmbGameID.removeAllItems();
        try {
            rs = dbCon.executeStatement("SELECT DISTINCT game_game_ID, title FROM game_copy, game WHERE game_game_ID = game_ID AND game_game_ID NOT IN (SELECT DISTINCT game_ID FROM rent_copy WHERE customer_ID = " + customer_ID + ") ORDER BY game_game_ID ASC");
            try {
                
                // populate combo boxes
                ItemListener listener = cmbGameID.getItemListeners()[0];
                cmbGameID.removeItemListener(listener);
                while (rs.next()) {
                    cmbGameID.addItem(rs.getInt("game_game_ID") + ": " + rs.getString("title"));
                }
                rs.close();
                
                cmbGameIDItemStateChanged(null);
                cmbGameID.addItemListener(listener);
                
                
            } catch (SQLException ex) {
                Logger.getLogger(RentGame.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        } catch (SQLException e) {
            javax.swing.JLabel label = new javax.swing.JLabel("SQL Error - Display selected game copy.");
            label.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 18));
            JOptionPane.showMessageDialog(null, label, "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }
    
