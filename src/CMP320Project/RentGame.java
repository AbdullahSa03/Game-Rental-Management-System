    private void btnRentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRentActionPerformed
        // TODO add your handling code here:
        
        try {
            if (cmbBranchID.getSelectedItem() != null && cmbGameID.getSelectedItem() != null) {
                String branch_ID = cmbBranchID.getSelectedItem().toString().split(": ")[0];
                String game_ID = cmbGameID.getSelectedItem().toString().split(": ")[0];
                
                LocalDate rentDate = LocalDate.now();
                LocalDate returnDate = rentDate.plusWeeks(1);
                
                // Define a formatter for the desired format
                DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
                // Format the current date using the formatter
                String formattedRentDate = rentDate.format(formatter);
                String formattedReturnDate = returnDate.format(formatter);
                
                String stmtSQL1 = "INSERT INTO rent_copy (customer_ID, game_ID, branch_ID, RENT_DATE, RETURN_DATE) VALUES ((SELECT customer_ID FROM customer WHERE name LIKE '" + LoginForm.currentMenu.luser.Name + "'), " + game_ID + ", " + branch_ID
                        + ", TO_DATE('" + formattedRentDate + "', 'YYYY-MM-DD'), TO_DATE('" + formattedReturnDate + "', 'YYYY-MM-DD'))";
                
                String stmtSQL2 = "UPDATE game_copy SET num_of_copies = (num_of_copies - 1) WHERE game_game_ID = " + game_ID + " AND store_branch_branch_ID = " + branch_ID;
                int result1 = dbCon.executePrepared(stmtSQL1);
                int result2 = dbCon.executePrepared(stmtSQL2);
                if(result1 > 0 && result2 > 0){
                    
                    javax.swing.JLabel label;
                    label = new javax.swing.JLabel("Rent table and number of copies for branch " + branch_ID + " and game " + game_ID + " updated successfully." 
                            + "\nThe game has been rented for 1 week! (You have until " + formattedReturnDate + " to return the game!)");
                    label.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 18));
                    JOptionPane.showMessageDialog(null, label, "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                }
                else {
                    javax.swing.JLabel label = new javax.swing.JLabel("SQL Error - Updating rents and game copies.");
                    label.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 18));
                    JOptionPane.showMessageDialog(null, label, "ERROR", JOptionPane.ERROR_MESSAGE);
                }
                
            } else {
                javax.swing.JLabel label = new javax.swing.JLabel("Please select the branch and game ID's");
                label.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 18));
                JOptionPane.showMessageDialog(null, label, "ERROR", JOptionPane.ERROR_MESSAGE);

            }

        } catch (SQLException e) {

            JOptionPane.showMessageDialog(null, "Error updating game." + e.getMessage());

        }
        getNewData();
    }//GEN-LAST:event_btnRentActionPerformed

