    private void populateFields() {
        try {
            
            txtGameID.setText(rs.getString("game_ID"));
            txtGameTitle.setText(rs.getString("title"));
            txtGameRentalPrice.setText(rs.getString("rental_price"));
            cmbPublisher.setSelectedItem(rs.getString("publisher_name"));
            
            ResultSet rs1 = dbCon.executeStatement("SELECT developer_ID FROM develops WHERE game_ID = " + rs.getString("game_ID") + " ORDER BY developer_ID ASC");
            DefaultListModel<String> selectedDevList = new DefaultListModel<>();
            while (rs1.next()) {
                selectedDevList.addElement(rs1.getString("developer_ID"));
            }
            jListSelectedDeveloper.setModel(selectedDevList);
            rs1.close();
            
            rs1 = dbCon.executeStatement("SELECT developer_ID FROM developer ORDER BY developer_ID ASC");
            DefaultListModel<String> devList = new DefaultListModel<>();
            boolean flag;
            while (rs1.next()) {
                flag = false;
                for (int i = 0; i < selectedDevList.getSize(); i++) {
                    if (rs1.getString("developer_ID").trim().equalsIgnoreCase(selectedDevList.getElementAt(i).trim())) {
                        flag = true;
                        break;
                    }
                }
                if (!flag) devList.addElement(rs1.getString("developer_ID"));
            }
            jListDeveloper.setModel(devList);
            // autofill the text boxes with the values retrieved from the database
            EnableDisableButtons();
        } catch (SQLException ex) {
            Logger.getLogger(UpdateDeleteGame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

