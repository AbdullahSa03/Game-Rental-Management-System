    // validate all user entry before sending new game details to DB
    boolean isValidData() {
        clearErrorLabels();
        boolean result = true; // assume all details are true, set to false if otherwise 
        // you need to check format deails and make sure they are consistent with DB 
        if (txtGameID.getText().trim().isEmpty() || !isInteger(txtGameID.getText().trim()) || (Integer.parseInt(txtGameID.getText().trim()) / 1000 > 9)) {
            if (txtGameID.getText().trim().isEmpty()) {
                lblGameIDError.setText("Cannot be empty.");
            } else if (!isInteger(txtGameID.getText().trim())) {
                lblGameIDError.setText("Must be integer.");
            }
            else{
                lblGameIDError.setText("Must be 4 digits at most.");
            }
            lblGameIDError.setVisible(true);
            result = false;
        }

        if (txtGameTitle.getText().trim().isEmpty() || (txtGameTitle.getText().trim().length() > 50)) {
            if (txtGameTitle.getText().trim().isEmpty()) {
                lblGameTitleError.setText("Cannot be empty.");
            } else {
                lblGameTitleError.setText("Must be < 50 chars.");
            }
            lblGameTitleError.setVisible(true);
            result = false;
        }

        if (txtGameRentalPrice.getText().trim().isEmpty() || !(isInteger(txtGameRentalPrice.getText().trim()) || isDouble(txtGameRentalPrice.getText().trim())) || (Double.parseDouble(txtGameRentalPrice.getText().trim()) / 1000 > 9 )) {
            if (txtGameRentalPrice.getText().trim().isEmpty()) {
                lblGameRentalPriceError.setText("Cannot be empty.");
            } else if (!(isInteger(txtGameRentalPrice.getText().trim()) || isDouble(txtGameRentalPrice.getText().trim()))) {
                lblGameRentalPriceError.setText("Must be a number.");
            } 
            else if (Double.parseDouble(txtGameRentalPrice.getText().trim()) / 1000 > 9){
                lblGameRentalPriceError.setText("Must less than 1000.");
            }
            lblGameRentalPriceError.setVisible(true);
            result = false;
        }

        return result;
    }
    
    private boolean isDuplicate(int game_ID) throws SQLException {
        
        try {
            // if the user changes the game_ID (the game_ID in the text box is not the same as the game_ID that was shown to the user)
            if(game_ID != rs.getInt("game_ID")){
                //check if the game_ID exists in the DB
                String stmtSQL = "SELECT game_ID FROM game WHERE game_ID = " + game_ID;
                ResultSet newRS;
                newRS = dbCon.executeStatement(stmtSQL);
                // isBeforeFirst() returns false if there are no data in the resultset
                return newRS.isBeforeFirst();
            }
        } catch (SQLException ex) {
            Logger.getLogger(UpdateDeleteLoginUser.class.getName()).log(Level.SEVERE, null, ex);
        }
        return false;
    }                
    
