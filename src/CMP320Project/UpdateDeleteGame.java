    public static void sort(DefaultListModel<String> model) {
        // Step 1: Extract the elements from the DefaultListModel into a list
        List<String> list = new ArrayList<>();
        for (int i = 0; i < model.getSize(); i++) {
            list.add(model.getElementAt(i));
        }

        // Step 2: Sort the list using a custom comparator that parses the strings as integers
        Collections.sort(list, new Comparator<String>() {
            @Override
            public int compare(String o1, String o2) {
                return Integer.compare(Integer.parseInt(o1), Integer.parseInt(o2));
            }
        });

        // Step 3: Clear the DefaultListModel
        model.clear();

        // Step 4: Add the sorted elements back into the DefaultListModel
        for (String element : list) {
            model.addElement(element);
        }
    }
    
