    // used to check if the two strings have a common character
    public static boolean checkCommonCharacter(String str1, String str2) {
        for (int i = 0; i < str1.length(); i++) {
            char c = str1.charAt(i);
            if (str2.indexOf(c) != -1) { //if there is a match
                return true;
            }
        }
        return false;
    }
    
    boolean isValidPassword() {
        // check validity of new password in case of update 
        boolean result = false;
        String password = txtPassword.getText().trim();
        
        //password should be between 5 and 25 characters and must include a special char or a capital letter
        if(password.length() >= 5 && password.length() <= 25 && checkCommonCharacter(password, chars)){
            result = true;
        }
        else{
            if (password.length() > 25) {
                lblPasswordError.setText("Must be less than 26 characters.");
            }
            else if (password.length() < 5){
                lblPasswordError.setText("Must be larger than 4 characters.");
            }
            else {
                lblPasswordError.setText("Must contain {!@#$%^&*()} or a Capital letter.");
            }
        }
        return result;
    }

    boolean isValidData() {
        clearErrorLabels();
        // check validity of new data before update 
        boolean result = true;        
        String name = txtName.getText().trim();
        String username = txtUsername.getText().trim();
        String password = txtPassword.getText().trim();
        String confirmPassword = txtConfirmPassword.getText().trim();
        
        if (name.isEmpty() || (name.length() > 25)) {
            if (name.isEmpty()) {
                lblNameError.setText("Cannot be empty.");
            } else {
                lblNameError.setText("Must be less than 26 characters.");
            }
            lblNameError.setVisible(true);
            result = false;
        }
        
        if (username.isEmpty() || (username.length() > 25)) {
            if (username.isEmpty()) {
                lblUsernameError.setText("Cannot be empty.");
            } else {
                lblUsernameError.setText("Must be less than 26 characters.");
            }
            lblUsernameError.setVisible(true);
            result = false;
        }
        
        if (!password.isEmpty() && !isValidPassword()) {
            // if the user does not type in a new password, then keep the old one

            lblPasswordError.setVisible(true);
            result = false;  
        }
        
        if (!password.isEmpty() && (!(confirmPassword.equals(password)) || confirmPassword.isEmpty())) {
            // if the user does not type in a new password, then keep the old one
            if (confirmPassword.isEmpty()) {
                lblConfirmPasswordError.setText("Must confirm the password.");
                lblConfirmPasswordError.setVisible(true);
                result = false;
            } else {
                // if the twoe passwords don't match
                lblConfirmPasswordError.setText("Must match the password.");
                lblConfirmPasswordError.setVisible(true);
                result = false;
            }
        }
        return result;
    }

