    private boolean isDuplicate(String username){
        try {
            // if the user changes the username (the username in the text box is not the same as the username that was shown to the user)
            if(!(username.equals(rs.getString("username")))){
                //check if the username exists in the DB
                String stmtSQL = "SELECT username FROM loginusers WHERE username = '" + username + "'";
                ResultSet newRS;
                newRS = dbCon.executeStatement(stmtSQL);
                // isBeforeFirst() returns false if there are no data in the resultset
                return newRS.isBeforeFirst();
            }
        } catch (SQLException ex) {
            Logger.getLogger(UpdateDeleteLoginUser.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return false;
    }

